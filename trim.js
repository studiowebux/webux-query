"use strict";

const isEmptyObject = obj => {
  return !Object.keys(obj).length;
};

module.exports = (select, limitation, validSelect) => {
  try {
    if (select && limitation) {
      limitation.forEach(element => {
        delete select[element];
      });
    }
    if (validSelect && isEmptyObject(select)) {
      return validSelect;
    } else {
      return select;
    }
  } catch (e) {
    return false;
  }
};
